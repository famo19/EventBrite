class EventObj:
    def __init__(self, idEventos, idUsuarios, nombre, categoria, fecha, hora, descripcion, valorEntrada, capacidad, disponibilidad, ciudad, pais, direccion, tipo):
        self.idEventos = idEventos
        self.idUsuarios = idUsuarios
        self.nombre = nombre
        self.categoria = categoria
        self.fecha = fecha
        self.hora = hora
        self.descripcion = descripcion
        self.valorEntrada = valorEntrada
        self.capacidad = capacidad
        self.disponibilidad = disponibilidad
        self.ciudad = ciudad
        self.pais = pais
        self.direccion = direccion
        self.tipo = tipo
